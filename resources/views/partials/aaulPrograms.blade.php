            <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapseTwo" class="collapsed">Home Repair Services <i class="fa fa-angle-down pull-right icon-collapsed"></i><i class="fa fa-angle-up pull-right icon-expanded"></i></a>
            </h4>
        </div>
        <div id="collapseTwo" class="panel-collapse collapse">
            <div class="panel-body">
                <p>This program provides emergency and critical repairs to owner occupied homes located in the City of Austin. The homeowners assisted by this program must reside in the City of Austin and their incomes may not exceed 80% of the Median Family Income (MFI) as determined by the U.S. Department of Housing and Urban Development (HUD). Services under this program include emergency repairs, customer recruitment, screening and selection of eligible customers, and the supervision of repairs on homes owned by low-income residents.</p>
                <p>Three categories are used to describe repairs:</p>
                <ul>
                    <li><strong>Crisis Intervention Assistance:</strong> repairs to homes which would alleviate those situations which present life threatening conditions such as leaking gas pipes or electrical wire short-circuiting.</li>
                    <li><strong>Emergency Repair Assistance:</strong> repairs to homes, which would alleviate threats to health and safety of occupants.</li>
                    <li><strong>Major System Repairs:</strong> repairs to or replacement of major operating system(s) of a home, when the repair does not alter environmental conditions and when the repair is limited to protection, repair or restoration activities necessary to control or arrest the effects of disasters, imminent threats or physical deterioration. Major systems are identified as electrical, plumbing, roofing, mechanical and heating systems.</li>
                </ul>
                <p><strong>Go Repair – General Obligation Bond Home Repairs Program (GOB-HRP)</strong></p>
                <p>The purpose of the General Obligation Bond Home Repairs Program is to address substandard housing conditions of low and moderate income homeowners residing within the corporate city limits of the City of Austin. The program provides financial assistance to make repairs that will eliminate health and safety hazards and/or provide improved accessibility. The following are examples of the type of repairs eligible for Go Repair:</p>
                <ul>
                    <li>Accessibility modifications such as ramps, grab bars and hand rails installation</li>
                    <li>Electrical, plumbing, mechanical repairs</li>
                    <li>Window and door repairs/replacements</li>
                    <li>Drywall and flooring repair</li>
                    <li>Painting as related to repairs</li>
                    <li>Roof replacement</li>
                </ul>
