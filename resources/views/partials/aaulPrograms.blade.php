                <p>This service program provides one-on-one assistance in the areas of case management, computer training, employability workshops, professional development and job placement assistance. The Life Skills Program is designed to assist clients in altering those negative patterns of behaviors that create barriers to their success. A variety of services are offered including basic computer training and financial literacy.</p>
                <p>The Austin Area Urban League’s Essential Office Skills (EOS) Academy equips clients with the essential skills to become gainfully employed and successfully prepares them for entrance into the workplace.</p>
                <p>The curriculum includes business communications essentials such as memoranda and business letter writing, verbal communication, reference material usage, as well as office technology sessions whereby students are exposed to electronic research, multi-line telephone, fax, copier, and postage machine utilization and filing. Students also participate in job readiness seminars conducted, selected business site visits and mock interviews.</p>
